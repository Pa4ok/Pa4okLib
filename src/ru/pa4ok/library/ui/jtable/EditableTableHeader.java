package ru.pa4ok.library.ui.jtable;

import ru.pa4ok.library.util.DataFilter;

public class EditableTableHeader
{
    private String header;
    private boolean autoGenerated;
    private boolean editable;
    private DataFilter<String> filter;

    public EditableTableHeader(String header, boolean autoGenerated, boolean editable, DataFilter<String> filter) {
        this.header = header;
        this.autoGenerated = autoGenerated;
        this.editable = editable;
        this.filter = filter;
    }

    public EditableTableHeader(String header, boolean autoGenerated, boolean editable) {
        this(header, autoGenerated, editable, DataFilter.allowFilter);
    }

    public EditableTableHeader(String header, DataFilter<String> filter) {
        this(header, false, true, filter);
    }

    public EditableTableHeader(String header) {
        this(header, false, true, DataFilter.allowFilter);
    }

    public String getHeader() {
        return header;
    }

    public void setHeader(String header) {
        this.header = header;
    }

    public boolean isEditable() {
        return editable;
    }

    public void setEditable(boolean editable) {
        this.editable = editable;
    }

    public DataFilter<String> getFilter() {
        return filter;
    }

    public void setFilter(DataFilter<String> filter) {
        this.filter = filter;
    }

    public boolean isAutoGenerated() {
        return autoGenerated;
    }

    public void setAutoGenerated(boolean autoGenerated) {
        this.autoGenerated = autoGenerated;
    }
}